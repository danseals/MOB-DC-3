class Car {
    
    var wheels: Int
    var doors: Int
    var name: String
    
    // Initialize Methood (aka: Initializer Function)
    init(wheels: Int, doors: Int, name: String) {
        self.wheels = wheels
        self.doors = doors
        self.name = name
    }
}

